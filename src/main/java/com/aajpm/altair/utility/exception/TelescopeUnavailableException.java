package com.aajpm.altair.utility.exception;

public class TelescopeUnavailableException extends TelescopeException {
    public TelescopeUnavailableException() {
        super();
    }

    public TelescopeUnavailableException(String msg) {
        super(msg);
    }

    public TelescopeUnavailableException(String msg, Throwable cause) {
        super(msg, cause);
    }

    public TelescopeUnavailableException(Throwable cause) {
        super(cause);
    }
    
}
